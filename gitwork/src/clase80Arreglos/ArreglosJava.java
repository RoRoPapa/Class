package clase80Arreglos;
/*==============================ARREGLOS========================
 * -Es la estructura de datos mas simple en java,
 * =un arreglo de datos, basicamente es un conjunto de informacion
 * la cual permite agrupar normmalmente de un tipo en particular:
 * Podemos declarar arreglos ya sea de variables tipo primitivo o de tipo object
 * -Los arreglos en java inician con el indice "0" el ultimo indice del arreglo se puede encontrar como
 * el unmero indicado menos 1 ejemplo 0->7 serian length = 8 y el ultimo indice es 7
 * -No todos los elementos del arreglo deben contener valores, Por ejemplo si hay 10 elementos en un array pero 
 * solo son ocupados 7 espacios en el arreglo, los 3 elementos restantes tendran su valor por default, de dicho 
 * tipo de arreglo declarado(int,String,double, etc) en los objetos seria de tipo "null"
 * [{para poder modificar o leer algun elemento del arreglo, lo tenemos que hacer en funcion del lugar que ocupe
 * dicho valor en el indice del arreglo}}
 * */
