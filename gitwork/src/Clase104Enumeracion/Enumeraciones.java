package Clase104Enumeracion;
/*===================================================ENUMERACIONES EN JAVA=========================================
 * los tipos enumerados son un tipo especial de clase, los cuales nos permiten asociar una serie de valores de tipo final 
 * es decir "SON CONSTANTES!"
 * Definicion de una enumeracion en java:
 * 
 * public enum Dias{
 * 
 *  //los valores son constantes por eso se escriben en MAYUSCULAS
 *  
 *   LUNES,
 *   MARTES,
 *   MIERCOLES,
 *   JUEVES,
 *   VIERNES,
 *   SABADO,
 *   DOMINGO,
 * }
 * Se utiliza la palabra reservada "enum"
 * Devido a que los elementos de una enumeracion son constantes se escriben con mayusculas, 
 * Las clases de tipo enumeracion las usaremos cada vez que necesitemos declarar una serie de constantes que ya
 * conoscamos previamente 
 * */
public enum Enumeraciones {
	
		LUNES,
	    MARTES,
	    MIERCOLES,
	    JUEVES,
	    VIERNES,
	    SABADO,
	    DOMINGO,
	
}
